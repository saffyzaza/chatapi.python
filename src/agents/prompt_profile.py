INSIGHT_RESPONSE_BLUEPRINT = """
ผู้อ่านเป้าหมาย: ผู้บริหารสำนักงานสาธารณสุขจังหวัด (สสจ.) — ต้องการรายงานระดับทางการที่อ่านแล้วตัดสินใจได้ทันที

โครงสร้างรายงาน (ใช้ชุดเดียว ห้ามสร้างหัวข้อซ้ำ):

## สรุปผู้บริหาร
เขียนเป็นย่อหน้า 3-5 ประโยคในภาษาทางการ ระบุ: ประเด็นหลักคืออะไร, ตัวเลขสำคัญ (แสดง 2 ทศนิยม), ช่วงเวลา, พื้นที่
ถ้าใช้ค่าประมาณ ให้ระบุทันทีว่า "ค่าประมาณจาก..." ในประโยคแรก แทนที่จะขึ้นต้นด้วย "เนื่องจากไม่มีข้อมูล"

## แหล่งข้อมูล
- ชื่อชุดข้อมูลที่ใช้จริง + ช่วงปี (ตัดนามสกุล .csv)
- ถ้ามีไฟล์ที่ไม่ได้ใช้ ระบุสั้นๆ ในบรรทัดเดียวว่า "ไม่ได้ใช้: [ชื่อ] (เหตุผล)"

## ผลลัพธ์
ตาราง markdown จากข้อมูลจริงใน Execution Result โดย:
- ชื่อคอลัมน์ต้องเป็นภาษาไทยที่อ่านได้สมบูรณ์ เช่น "ร้อยละเด็กเริ่มอ้วน ช่วง 12-18 ปี (ประมาณ)" แทน "เริ่มอ้วน_%*12-18"
- ตัวเลขทศนิยมแสดง 2 ตำแหน่งในตาราง
- ถ้าใช้ค่าประมาณ ติดป้าย (ประมาณ) ท้ายชื่อคอลัมน์
- ใต้ตาราง: เขียนย่อหน้า 2-4 ประโยคอธิบายความหมายและวิธีอ่านตาราง

## วิธีคำนวณ
อธิบายเป็นย่อหน้าชัดเจน 2-5 ประโยค ระบุ dataset ที่ใช้ คอลัมน์ต้นทาง และวิธีการ
ถ้าใช้ interpolation/proxy ให้แสดงสูตรในรูปแบบ LaTeX ดังนี้:

$$\\hat{v}_{12-18} = v_{6-14} + \\frac{(v_{19-59} - v_{6-14}) \\times (15 - 6)}{19 - 6}$$

(ปรับตัวแปรและตัวเลขตามจริง)

## ผลการวิเคราะห์และข้อค้นพบ
เขียนเป็นย่อหน้าหรือ bullet 3-5 ข้อ แต่ละข้อต้องอิงตัวเลขจริงจากตาราง เช่น
- "ร้อยละเด็กอ้วนในกลุ่มอายุ 12-18 ปี มีแนวโน้มเพิ่มขึ้นต่อเนื่อง จาก 3.58% ในปี 2565 เป็น 5.86% ในปี 2569 คิดเป็นการเพิ่มขึ้น 2.28 จุดร้อยละใน 5 ปี"

## ข้อเสนอแนะเชิงนโยบาย (ไม่เกิน 4 ข้อ)
เขียนเป็นย่อหน้าสั้น แต่ละข้อต้องโยงกับตัวเลขที่พบ และระบุหน่วยงานรับผิดชอบหรือมาตรการที่แนะนำ

## ข้อจำกัดและข้อควรระวัง
ระบุช่วงที่ขาดหาย ระดับความน่าเชื่อถือของค่าประมาณ และผลกระทบต่อการตีความ (1-3 ข้อ)

กฎห้ามละเมิด:
- ห้ามหัวข้อซ้ำ ห้ามตารางซ้ำท้ายคำตอบ ห้าม HTML tag
- ห้ามขึ้นต้นย่อหน้าด้วย "เนื่องจากไม่มีข้อมูล" — ให้บอกว่า "ใช้ค่าประมาณจาก X" แทน
- ต้องตอบตรงขอบเขต ปี/พื้นที่/ช่วงอายุ ที่ผู้ใช้ถาม
- ชื่อคอลัมน์ในตารางต้องเป็นภาษาไทยอ่านได้ ห้ามใช้ชื่อตัวแปรดิบ
- ตัวเลขในตารางต้องแสดง 2 ทศนิยม พร้อมหน่วย (%) ถ้าเป็นร้อยละ
- สมการ LaTeX ($$...$$) ต้องอยู่บรรทัดเดียวโดดๆ เสมอ มีบรรทัดว่าง 1 บรรทัดก่อนและหลัง ห้ามฝัง $$...$$ ไว้กลางประโยคหรือท้ายประโยค
""".strip()

