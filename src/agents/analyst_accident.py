"""Accident Domain Analyst Agent: analyzes retrieved accident data."""
from crewai import Agent


def create_accident_analyst(llm) -> Agent:
    return Agent(
        role="Accident Data Analyst",
        goal="วิเคราะห์ข้อมูลอุบัติเหตุที่ค้นมาได้ สังเคราะห์ประเด็นสำคัญ ระบุจุดเสี่ยง แนวโน้ม และกลุ่มเสี่ยง",
        backstory=(
            "คุณเป็นนักวิเคราะห์ข้อมูลอุบัติเหตุที่มีความเชี่ยวชาญ "
            "คุณสามารถตีความข้อมูลสถิติอุบัติเหตุ ระบุจุดเสี่ยง ช่วงเวลาเสี่ยง "
            "วิเคราะห์ปัจจัยที่เกี่ยวข้อง เช่น สภาพถนน สภาพอากาศ ประเภทยานพาหนะ "
            "และเสนอแนวทางป้องกันบนพื้นฐานหลักฐานเชิงข้อมูล "
            "คุณต้องแยก 'ข้อเท็จจริง' ออกจาก 'การตีความ' อย่างชัดเจน "
            "และห้ามสรุปเกินหลักฐานที่มี"
        ),
        llm=llm,
        verbose=True,
        allow_delegation=False,
    )


